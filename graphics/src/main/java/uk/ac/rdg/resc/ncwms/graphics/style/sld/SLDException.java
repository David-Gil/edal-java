package uk.ac.rdg.resc.ncwms.graphics.style.sld;

public class SLDException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5465038077417251267L;

	public SLDException() {
	}

	public SLDException(String arg0) {
		super(arg0);
	}

	public SLDException(Throwable arg0) {
		super(arg0);
	}

	public SLDException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SLDException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
