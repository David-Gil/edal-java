package uk.ac.rdg.resc.godiva.client.widgets;

public class ScreenPosition {
    private int x;
    private int y;

    public ScreenPosition(int x, int y) {
        super();
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
